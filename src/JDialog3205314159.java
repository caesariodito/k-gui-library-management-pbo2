    public boolean isiData(Peminjam205314159 pem) throws SQLException {
        if (umumB.isSelected()) { //masyarakat umum
            if (!((MasyarakatUmum205314159) pem).getNo_ktp().matches("\\d{16}")) {
                JOptionPane.showMessageDialog(this, "NIK harus 16 digit!\n", "ERROR", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } else if (mhsB.isSelected()) {  //mahasiswa
            if (!((Mahasiswa205314159) pem).getNo_mhs().matches("\\d{9}")) {
                JOptionPane.showMessageDialog(this, "NIM harus 9 digit!\n", "ERROR", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } else {    //dosen
            if (!((Dosen205314159) pem).getNip().matches("\\d{18}")) {
                JOptionPane.showMessageDialog(this, "NIP harus 18 digit!\n", "ERROR", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        }
        //method normal
//        dtPerpus.peminjamList.add(pem);
        //menggunakan db
        
        boolean update = false;
        for (Peminjam205314159 peminjam : dtPerpus.peminjamList) {
            if(peminjam.getId().equals(pem.getId()))
                update = true;
        }
        if (update) {
            dtPerpus.updatePeminjam(pem);
        } else {
            dtPerpus.isiDataPeminjam(pem);
        }

        return true;
    }

