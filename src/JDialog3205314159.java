    private void simpanBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBActionPerformed
        // TODO add your handling code here:
        // BEGIN CODE INITIALIZE
        String nomor = nomorTF.getText();
        String id = idTF.getText();
        String nama = namaTF.getText();
        String alamat = alamatTA.getText();
        int maksPinjam;

        // BEGIN exception handling
        try {
            maksPinjam = Integer.parseInt(maksPinjamTF.getText());
            setMaksPinj(maksPinjam);

            if (!(id.matches("^[MDU]\\d+"))) {
                throw new Exception("ID harus diawali M/D/U!");
            }
            if (nama.matches(".*\\d.*")) {
                throw new Exception("Nama anda mengandung angka: " + nama);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "'"
                    + maksPinjamTF.getText() + "' is invalid\n"
                    + "Please enter digits number only", "ERROR", JOptionPane.WARNING_MESSAGE);
            return;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // END exception handling
        // BEGIN input data to Table
        try {
            if (umumB.isSelected()) {
                Peminjam205314159 peminjamNew = new MasyarakatUmum205314159(id, nama, alamat, maksPinjam, nomor);
                if (isiData(peminjamNew)) {
                    JOptionPane.showMessageDialog(this, "Data MasyarakatUmum berhasil disimpan!\n" + peminjamNew.toString(), "Information", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    return;
                }
            } else if (mhsB.isSelected()) {
                Peminjam205314159 peminjamNew = new Mahasiswa205314159(id, nama, alamat, maksPinjam, nomor);
                if (isiData(peminjamNew)) {
                    JOptionPane.showMessageDialog(this, "Data Mahasiswa berhasil disimpan!\n" + peminjamNew.toString(), "Information", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    return;
                }
            } else {
                Peminjam205314159 peminjamNew = new Dosen205314159(id, nama, alamat, maksPinjam, nomor);
                if (isiData(peminjamNew)) {
                    JOptionPane.showMessageDialog(this, "Data Dosen berhasil disimpan!\n" + peminjamNew.toString(), "Information", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    return;
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        // END input data to Table
        // END CODE INITIALIZE
        // RESET
        tampilData();
        resetBActionPerformed(evt);
    }//GEN-LAST:event_simpanBActionPerformed

