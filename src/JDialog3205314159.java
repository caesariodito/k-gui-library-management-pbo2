    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int a = jTable1.getSelectedRow();
            if (a == -1) {
            } else {
                Peminjam205314159 delete = dtPerpus.peminjamList.get(a);
                if (delete instanceof Dosen205314159) {
                    dtPerpus.deleteDataPeminjam(delete.getId(), "Dosen");
                } else if (delete instanceof Mahasiswa205314159) {
                    dtPerpus.deleteDataPeminjam(delete.getId(), "Mahasiswa");
                } else {
                    dtPerpus.deleteDataPeminjam(delete.getId(), "Umum");
                }
            }
            resetBActionPerformed(evt);
            tampilData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

